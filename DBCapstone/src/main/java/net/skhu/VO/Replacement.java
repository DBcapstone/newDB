package net.skhu.VO;

public class Replacement {

	private int id;
	private String largeCategory;
	private int departmentId;
	private String departmentName;
	private String abolishCode;
	private String abolishName;
	private String category;
	private int credits;
	private String replacementCode;
	private String replacementName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getLargeCategory() {
		return largeCategory;
	}
	public void setLargeCategory(String largeCategory) {
		this.largeCategory = largeCategory;
	}
	public int getDepartmentId() {
		return departmentId;
	}
	public void setDepartmentId(int departmentId) {
		this.departmentId = departmentId;
	}
	public String getDepartmentName() {
		return departmentName;
	}
	public void setDepartmentName(String departmentName) {
		this.departmentName = departmentName;
	}
	public String getAbolishCode() {
		return abolishCode;
	}
	public void setAbolishCode(String abolishCode) {
		this.abolishCode = abolishCode;
	}
	public String getAbolishName() {
		return abolishName;
	}
	public void setAbolishName(String abolishName) {
		this.abolishName = abolishName;
	}
	public String getCategory() {
		return category;
	}
	public void setCategory(String category) {
		this.category = category;
	}
	public int getCredits() {
		return credits;
	}
	public void setCredits(int credits) {
		this.credits = credits;
	}
	public String getReplacementCode() {
		return replacementCode;
	}
	public void setReplacementCode(String replacementCode) {
		this.replacementCode = replacementCode;
	}
	public String getReplacementName() {
		return replacementName;
	}
	public void setReplacementName(String replacementName) {
		this.replacementName = replacementName;
	}


}
