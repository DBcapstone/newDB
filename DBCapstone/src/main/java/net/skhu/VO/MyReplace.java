package net.skhu.VO;

public class MyReplace {
	private int id;
	private String studentId;
	private String abolishCode;
	private String abolishName;
	private String replacementCode;
	private String replacementName;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getStudentId() {
		return studentId;
	}
	public void setStudentId(String studentId) {
		this.studentId = studentId;
	}
	public String getAbolishCode() {
		return abolishCode;
	}
	public void setAbolishCode(String abolishCode) {
		this.abolishCode = abolishCode;
	}
	public String getAbolishName() {
		return abolishName;
	}
	public void setAbolishName(String abolishName) {
		this.abolishName = abolishName;
	}
	public String getReplacementCode() {
		return replacementCode;
	}
	public void setReplacementCode(String replacementCode) {
		this.replacementCode = replacementCode;
	}
	public String getReplacementName() {
		return replacementName;
	}
	public void setReplacementName(String replacementName) {
		this.replacementName = replacementName;
	}
}

